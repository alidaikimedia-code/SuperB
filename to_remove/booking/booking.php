        // Array to store selected values
        let selectedValues = [];

        // Function to toggle button selection and update the link
        function toggleValue(value, button) {
            const index = selectedValues.indexOf(value);

            if (index === -1) {
                // Add value and highlight the button
                selectedValues.push(value);
                button.classList.add("selected");
            } else {
                // Remove value and unhighlight the button
                selectedValues.splice(index, 1);
                button.classList.remove("selected");
            }

            updateLink();
        updateSelectedValuesDisplay();
}

// Function to update the displayed selected values in a <span>
function updateSelectedValuesDisplay() {
    const selectedSpan = document.getElementById("selected-values");
    if (selectedSpan) {
        selectedSpan.textContent = selectedValues.length > 0
            ? `Selected: ${selectedValues.join(", ")}`
            : "No selections yet.";
    }
}
